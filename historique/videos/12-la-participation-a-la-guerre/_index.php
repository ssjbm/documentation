<h4>La SSJB-M et la participation à la guerre: 1939-1945</h4>

<p>Réunie en congrès le 9 mars 1939, la SSJB a voté une résolution qui demandait au gouvernement canadien de se comporter en État libre, de ne pas s’engager dans l’effort de guerre et de refuser toute participation à des guerres extérieures.</p>

