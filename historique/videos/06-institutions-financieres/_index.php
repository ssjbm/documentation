<h4>La création d’institutions financières</h4>

<p>Depuis la conquête britannique L’économie du Québec était dominée par des anglophones et les Canadiens français était confiné au rôle de porteur d’eau. L’agriculture ne permettait pas l’accumulation du capital suffisante pour investir dans des usines. L’accès au capital financier avait toujours été une entrave au développement d’une bourgeoisie d’affaire francophones.</p>

<p>C’est ce que la SSJB comprendra après la première guerre mondiale. Elle voudra changer le rapport de force en créant des institutions financières comme la Société nationale de fiducie, la Société nationale d’Assurances, la Société nationale de colonisation.</p>

