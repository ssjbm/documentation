<h4>La lutte contre la centralisation: 1937-1957</h4>

<p>Suite à la crise des années trente et de la participation du canada à la 2e guerre mondiale, le gouvernement fédéral entreprit un processus de centralisation des pouvoirs en envahissant le champ des politiques sociales de la culture et de l’éducation et en s’emparant des pouvoirs d taxation. La SSJB-M se mobilisa pour défendre l’autonomisme provincial.</p>

