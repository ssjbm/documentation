<h4>L’émancipation par l’éducation: la fondation du prêt d’honneur: 1947-1971</h4>

<p>Fondée pour rendre le peuple meilleur, la SSJB a toujours cherché à répandre l’éducation parmi les Canadiens français. L’éducation était considérée comme sa mission principale parce qu’elle représentait la condition essentielle au relèvement de la nation. À cette fin, dans l’après-guerre, la Société a créé la fondation du prêt d’honneur pour aider les étudiants à financer leurs études.</p>

