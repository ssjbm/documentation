<h4>L’émancipation économique des Canadiens français 1885-1914</h4>

<p>Avec l’élection de Laurent-Olivier David à la présidence en 1889, la SSJB-M commence à se préoccuper de la situation économique des Canadiens français. Dans cette quatrième conférence, j’analyse la stratégie d’affirmation économique des Canadiens français mise de l’avant par la SSJB qui se manifeste par la création d’institutions comme la Chambre de commerce de Montréal, l’École des hautes études commerciales et le Monument national.</p>

