<h4>Vers l’indépendance: les États généraux du Canada français</h4>

<p>La renaissance du mouvement indépendantiste à la fin des années cinquante se fit à l’extérieur du mouvement nationaliste. La SSJB manifeste une certaine circonspection devant la tournure que prend la Révolution tranquille. Autant elle appuie les efforts du gouvernement Lesage pour réformer le fédéralisme canadien dans le sens d’une plus grande autonomie, autant elle exprime ses réticences devant les transformations sociales qui bouleversent l’ordre traditionnel. Elle fera preuve de prudence et accompagnera les changements plus qu’elle ne les initiera. Elle sera en fait poussée à évoluer par l’activisme des partis indépendantistes qui redéfinissent les enjeux de la question nationale en préconisant l’indépendance et l’unilinguisme français.</p>

